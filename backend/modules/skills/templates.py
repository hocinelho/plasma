# --------------------------------------------------------------------------
# Registry of available templates
# --------------------------------------------------------------------------
TEMPLATES: list[Template] = [
    Template(
        name="weather",
        description="Current weather lookup for any city.",
        keywords=["weather", "temperature", "forecast", "rain", "sunny"],
        default_triggers=["weather", "what's the weather", "weather in"],
        render=_render_weather,
    ),
    Template(
        name="calculator",
        description="Basic arithmetic.",
        keywords=["plus", "minus", "times", "divided", "calculate", "compute", "what is"],
        default_triggers=["calculate ", "compute ", "what is ", "how much is "],
        render=_render_calculator,
    ),
    Template(
        name="joke",
        description="Tells a joke.",
        keywords=["joke", "funny", "make me laugh"],
        default_triggers=["tell me a joke", "make me laugh", "tell a joke"],
        render=_render_joke,
    ),
    Template(
        name="timer",
        description="Sets a timer (acknowledgment only).",
        keywords=["timer", "minutes", "seconds", "alarm"],
        default_triggers=["set a timer", "timer for", "remind me in"],
        render=_render_timer,
    ),
]


def find_template_for(text: str) -> Template | None:
    """Return the best-matching template by keyword overlap, or None."""
    lower = text.lower()
    best: Template | None = None
    best_score = 0
    for tpl in TEMPLATES:
        score = sum(1 for kw in tpl.keywords if kw in lower)
        if score > best_score:
            best = tpl
            best_score = score
    return best if best_score > 0 else None